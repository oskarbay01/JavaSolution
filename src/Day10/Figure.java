package Day10;


public class Figure {
    public double a;
    public double b;

    public Figure(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double area(){
        return 0;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
}
